    private void pullBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pullBtnActionPerformed
        // TODO add your handling code here:
        int pullAmount = Integer.parseInt(pullTF.getText());
        if(pullAmount < 0){
            JOptionPane.showMessageDialog(this, "Penarikan tidak boleh negatif");
        } else if(pullAmount < 1){
            JOptionPane.showMessageDialog(this, "Penarikan tidak boleh nol");
        } else{
            pullAmount *= -1;
            if(checkValidPull(currentId,pullAmount)){
                conDB.userPullBalance(currentId, pullAmount);
                reloadData();
                pullTF.setText("");
            } else{
                JOptionPane.showMessageDialog(this, "Tidak dapat melakukan penarikan lebih dari saldo : " + conDB.getLastBalance(currentId));
                pullTF.setText("");
            }
        }
        
        balSUMAmountLbl.setText("" + conDB.getLastBalance(currentId));
    }//GEN-LAST:event_pullBtnActionPerformed

    private boolean checkValidPull(int curID, int amon){
        conDB.connect();
        if(conDB.getLastBalance(curID) + amon  >= 0){
            return true;
        } else{
            return false;
        }
    }
    
    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        int pushAmount = Integer.parseInt(addTF.getText());
        conDB.connect();
        if(pushAmount < 0){
            JOptionPane.showMessageDialog(this, "Penambahan tidak boleh negatif");
        } else if(pushAmount < 1){
            JOptionPane.showMessageDialog(this, "Penambahan tidak boleh nol");
        } else{
            conDB.userPushBalance(currentId, pushAmount);
        }
        
        reloadData();
        addTF.setText("");
        balSUMAmountLbl.setText("" + conDB.getLastBalance(currentId));
    }//GEN-LAST:event_addBtnActionPerformed

