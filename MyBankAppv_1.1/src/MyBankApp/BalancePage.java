    public void showAllData(int itu){
        
        ArrayList<BalanceData> bd = new ArrayList<BalanceData>();
        bd = conDB.getUserBalance(itu);
        for(int i = 0; i < bd.size(); i++){
            models.insertRow(models.getRowCount(), new Object[]{bd.get(i).getDate(),bd.get(i).getPrevBalance(), bd.get(i).getAfterBalance(), bd.get(i).getTransaction()});
        }
    }
    
