    private void loadUser(){
        ArrayList<User> ua = new ArrayList<User>();
        ua = conDB.getAllUser();
        for(int i = 0; i < ua.size(); i++){
            models.insertRow(models.getRowCount(), new Object[]{ua.get(i).getId(),ua.get(i).getName()});
        }
    }
    
