    private void delUserBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delUserBtnActionPerformed
        // TODO add your handling code here:
        int ids = 0;
        try{
            ids = Integer.parseInt(idInsertTF.getText());
        } catch(NumberFormatException e){
            
        }
        String names = nameInsertTF.getText();
        if(!names.equals("")){
            if(conDB.checkUsername(names) > 0){
                conDB.adminDeluser(names);
            } else{
                JOptionPane.showMessageDialog(this, "Nama tidak terdaftar");
            }
        } else if(ids != 0){
            if(!conDB.adminDeluser(ids)){
                JOptionPane.showMessageDialog(this, "ID tidak terdaftar");
            }
        }
        models.setRowCount(0);
        loadUser();
    }//GEN-LAST:event_delUserBtnActionPerformed

